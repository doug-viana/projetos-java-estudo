public class Ativo {
    private String ticker;
    private double preco;

    // Getters e Setters para manter o cofre seguro
    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
}
